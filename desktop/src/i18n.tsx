const dictionaries: Record<Language, Dictionary> = { "zh-CN": zhCN, ja, en };

interface I18nValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: MessageKey, variables?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    const saved = localStorage.getItem("armusic-language");
    return saved && saved in languageNames ? saved as Language : "zh-CN";
  });

  useEffect(() => {
    localStorage.setItem("armusic-language", language);
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<I18nValue>(() => ({
    language,
    setLanguage,
    t: (key, variables) => {
      let message = dictionaries[language][key] || zhCN[key];
      Object.entries(variables || {}).forEach(([name, replacement]) => {
        message = message.replaceAll(`{${name}}`, String(replacement));
      });
      return message;
    },
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}
